fun note1Sound(context: Context) {
    var swipeSound = MediaPlayer.create(context, R.raw.note1)
    swipeSound.setVolume(0.9f, 0.9f)
    swipeSound.start()
    swipeSound.setOnCompletionListener {
        swipeSound.release()
        swipeSound = null
    }
}
